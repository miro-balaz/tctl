long long FoxAndGCDLCM::get(long long G, long long L){
// A*B = L*G
// A=k*G
//B=l*G;
// L =G*k*l;
// k*l = L/G;
//minimize k+l  
if(L % G) return -1;
long long X= L/G;
long long rval=-1;
for(long long i=1;i*i<=X;++i) {
  if(X%i==0) {
    long long l=i;
    long long k=X/i;
    long long res=(k+l)*G;
    if(__gcd(l,k)==1 &&( rval==-1 || res<rval)) {
      rval=res;
    }
  }
}
  return rval;

}
